def _simulated_fallback_proposals(
    record: SignalRecord,
    chain: Sequence[OptionContractSnapshot],
    metrics: TickerMetrics,
    scanned_at: datetime,
) -> list[OptionProposal]:
    right: OptionRight = "CALL" if record.payload.direction == "long" else "PUT"
    underlying = metrics.current_price or record.payload.underlying_price
    candidates = [
        contract
        for contract in chain
        if contract.right == right and _contract_entry_price(contract) is not None
    ]
    if not candidates:
        return []

    if underlying is None:
        contract = min(candidates, key=lambda item: (item.expiry, item.strike))
    else:
        contract = min(
            candidates,
            key=lambda item: (
                item.expiry,
                0
                if (item.right == "CALL" and item.strike >= underlying)
                or (item.right == "PUT" and item.strike <= underlying)
                else 1,
                abs(item.strike - underlying),
                item.strike if item.right == "CALL" else -item.strike,
            ),
        )

    entry_price = _contract_entry_price(contract)
    if entry_price is None:
        return []
    quantity = 1
    debit = round(entry_price * 100 * quantity, 2)
    leg = OptionProposalLeg(
        action="BUY",
        qty=quantity,
        symbol=contract.symbol,
        broker_symbol=contract.broker_symbol,
        expiry=contract.expiry,
        strike=contract.strike,
        right=contract.right,
        price=entry_price,
        bid=contract.bid,
        ask=contract.ask,
        mark=contract.mark,
        delta=contract.delta,
        open_interest=contract.open_interest,
        volume=contract.volume,
    )
    proposal_id_basis = "|".join(
        [
            record.id,
            "sim_fallback_single",
            contract.symbol,
            contract.expiry.isoformat(),
            f"{contract.strike:g}",
            contract.right,
        ]
    )
    proposal_id = f"sim_prop_{hashlib.sha256(proposal_id_basis.encode('utf-8')).hexdigest()[:12]}"
    return [
        OptionProposal(
            id=proposal_id,
            signal_id=record.id,
            symbol=contract.symbol,
            direction=record.payload.direction,
            structure="single",
            created_at=scanned_at,
            expiry=contract.expiry,
            quantity=quantity,
            underlying_price=underlying,
            legs=[leg],
            debit=debit,
            max_loss=debit,
            natural_limit_price=entry_price,
            natural_debit=debit,
            send_limit_price=entry_price,
            price_protection="SIM ONLY - replay proposal, not eligible for Schwab order submission.",
            net_delta=contract.delta,
            score=0,
            tos_order_line=_sim_tos_order_line(contract, quantity, entry_price),
            reasons=["SIM_ONLY", "historical_equity_replay", "simulated_fallback_single"],
            notes=[
                "SIMULATION ONLY - blocked from Schwab order submission.",
                "Fallback used nearest current option-chain contract to Friday replay underlying price.",
                f"Current-chain quote timestamp: {contract.timestamp.isoformat()}",
            ],
        )
    ]


def _contract_entry_price(contract: OptionContractSnapshot) -> float | None:
    for value in (contract.ask, contract.mark, contract.last):
        if value is not None and value > 0:
            return round(float(value), 2)
    return None


def _sim_tos_order_line(contract: OptionContractSnapshot, quantity: int, limit_price: float) -> str:
    return (
        f"SIM ONLY BUY +{quantity} SINGLE {contract.symbol.upper()} 100 "
        f"{contract.expiry.strftime('%d %b %y').upper()} {_format_strike(contract.strike)} "
        f"{contract.right} @{limit_price:.2f} LMT"
    )


def _target_intraday_date(candles: list[Candle], preferred: date, tz: ZoneInfo) -> date | None:
    dates = [candle.timestamp.astimezone(tz).date() for candle in candles]
    if not dates:
        return None
    if preferred in dates:
        return preferred
    return max(dates)


def _vwap(candles: list[Candle]) -> float | None:
    total_volume = sum(candle.volume for candle in candles)
    if total_volume <= 0:
        return None
    total = sum(((candle.high + candle.low + candle.close) / 3) * candle.volume for candle in candles)
    return total / total_volume


def _session(value: datetime, timezone_name: str) -> ScanSession:
    local = value.astimezone(ZoneInfo(timezone_name))
    clock = local.time().replace(tzinfo=None)
    weekday = local.weekday()
    if weekday == 5:
        return "closed"
    if weekday == 6:
        return "overnight" if clock >= time(20, 0) else "closed"
    if weekday < 4 and clock >= time(20, 0):
        return "overnight"
    if weekday <= 4 and clock < time(4, 0):
        return "overnight"
    if weekday == 4 and clock >= time(20, 0):
        return "closed"
    if time(4, 0) <= clock < time(9, 30):
        return "premarket"
    if time(9, 30) <= clock < time(16, 0):
        return "regular"
    if time(16, 0) <= clock < time(20, 0):
        return "after_hours"
    return "closed"


def _data_freshness_notes(*, as_of: datetime, price_time: datetime | None, session: ScanSession) -> list[str]:
    if price_time is None:
        return ["missing_price_timestamp"]
    age_seconds = max(0, int((as_of - price_time).total_seconds()))
    if session == "overnight" and age_seconds > 900:
        return [
            f"stale_overnight_price:{price_time.isoformat()}",
            "schwab_rest_may_not_match_tos_24h_price",
        ]
    if session in {"premarket", "regular", "after_hours"} and age_seconds > 900:
        return [f"stale_price:{price_time.isoformat()}"]
    return []


def _scan_id(value: datetime) -> str:
    return value.strftime("scan_%Y%m%d_%H%M%S")


def _utc(value: datetime) -> datetime:
    if value.tzinfo is None:
        return value.replace(tzinfo=timezone.utc)
    return value.astimezone(timezone.utc)


def _round(value: float | int | None, places: int = 4) -> float | None:
    return None if value is None else round(float(value), places)


def _format_strike(strike: float) -> str:
    return str(int(strike)) if float(strike).is_integer() else str(strike)


def _add_business_days(as_of_date: date, days: int) -> date:
    if days <= 0:
        return as_of_date
    current = as_of_date
    remaining = days
    while remaining > 0:
        current += timedelta(days=1)
        if current.weekday() < 5:
            remaining -= 1
    return current


def _next_friday_after(as_of_date: date) -> date:
    days_until_friday = (4 - as_of_date.weekday()) % 7
    if days_until_friday == 0:
        days_until_friday = 7
    return as_of_date + timedelta(days=days_until_friday)
